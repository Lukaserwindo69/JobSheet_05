public class OperatorUnary {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int x = 1;
        int y = 0;
        
        y = ++x;
        System.out.println("Nilai Y Sekarang: " + y );
        System.out.println("Nilai X Sekarang: " + y );
    }
    
}
